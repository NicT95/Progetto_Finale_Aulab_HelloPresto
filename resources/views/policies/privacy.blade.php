            I. DATI PERSONALI CHE RACCOGLIAMO
            Riepilogo: raccogliamo i dati personali in tre modi principali:
            
            Alcuni dati vengono forniti direttamente dall’utente (ad es. registrandosi per un account).
            Alcuni dati vengono da noi registrati automaticamente (anche con tecnologie come i cookie).
            Alcuni dati ci pervengono da terze parti (come le aziende di social media).
            I particolari tipi di dati che raccogliamo dipendono in genere da come l’utente interagisce con noi, ad esempio quali Servizi di Riot utilizza e come.
            
            A. Dati personali forniti dall’utente
            Riepilogo: raccogliamo i dati che l’utente decide di fornirci. Non è obbligatorio rendere noti i dati quando viene richiesto, ma se l’utente si rifiuta, potrebbe non essere in grado di accedere a determinati Servizi di Riot o di usufruire di tutte le loro funzionalità.
            
            Espandi
            
            B. Dati che raccogliamo automaticamente
            Riepilogo: raccogliamo automaticamente alcuni dati sul modo in cui l’utente interagisce e naviga sui Servizi di Riot, così come sul dispositivo e sul software utilizzati per farlo.
            
            Espandi
            
            C. Dati che raccogliamo da terze parti
            Riepilogo: a volte otteniamo dati da terze parti da utilizzare insieme ai dati da noi raccolti. Ad esempio, se l’utente collega il suo account Facebook con il suo account Riot Games, Meta potrebbe condividere con noi i dati in conformità all'informativa sulla privacy di Facebook e alle impostazioni sulla privacy impostate dall’utente su tale piattaforma. Meta condivide con noi anche dati sull’utilizzo delle fan page di Riot Games ufficiali e specifiche dei giochi. Ad esempio, raccogliamo anche dati pubblicitari e analitici da terze parti per sostenere le nostre iniziative di marketing, migliorare i Servizi di Riot e gestire meglio le nostre campagne pubblicitarie.
            
